char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*str;

	str = (char *) s;
	i = 0;
	while (s[i] != '\0')
	{
		i++;
		str++;
	}
	while (i >= 0)
	{
		if (s[i] % 256 == c % 256)
			return (str);
		i--;
		str--;
	}
	return (0);
}
